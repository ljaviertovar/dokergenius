import React, { useState } from "react"

export default function EnvironmentConfig({ onPrev, onNext }) {
	const [port, setPort] = useState("")
	const [directory, setDirectory] = useState("")

	const handleSubmit = event => {
		event.preventDefault()
		onNext({ port, directory })
	}

	return (
		<div className='flex flex-col items-center justify-center h-full'>
			<h1 className='text-3xl font-bold mb-4'>Configuración del Entorno</h1>
			<form className='w-1/2' onSubmit={handleSubmit}>
				<div className='mb-4'>
					<label htmlFor='port' className='block text-lg font-medium mb-2'>
						Puerto de la aplicación
					</label>
					<input
						type='text'
						id='port'
						className='w-full px-3 py-2 border rounded-lg text-lg'
						value={port}
						onChange={e => setPort(e.target.value)}
						placeholder='Ej: 3000'
						required
					/>
				</div>
				<div className='mb-4'>
					<label htmlFor='directory' className='block text-lg font-medium mb-2'>
						Directorio de la aplicación
					</label>
					<input
						type='text'
						id='directory'
						className='w-full px-3 py-2 border rounded-lg text-lg'
						value={directory}
						onChange={e => setDirectory(e.target.value)}
						placeholder='Ej: /usr/src/app'
						required
					/>
				</div>
			</form>
		</div>
	)
}
